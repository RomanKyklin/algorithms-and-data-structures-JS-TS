/**
 * Quick Sort algorithms implemented
 * Overall time complexity of Insertion sort is O(n2)
 * @param {Array} arr
 * @return {Array}
 */
export const quickSort = (arr: number[]): number[] => {
    if (arr.length <= 1) return arr;


    return [];
}

console.log(quickSort([10, 5, 3, 8, 2, 6, 4, 7, 9, 1]));
